      <h3 class="display-5">Historia</h3>
      <p class="offset-xl-2 col-xl-8">Pomysł na powstanie drużyny powstał po rozegraniu meczu na jednym z turniejów Overwatch. Nazwa oraz pierwszy background wzięły się od pewnej akcji w wykonaniu jednego z zawodników. Niestety po pewnym czasie drużyna z Overwatcha zaczęła się rozpadać.
        Powodem był brak zgrania oraz brak chęci do dalszej gry. Ostatecznym ciosem dla całej drużyny okazało się odejście czołowego zawodnika tuż przed ważnym dla nich turniejem . Po rozegraniu ostatniego turnieju Overwatch Open Division, drużyna całkowicie
        się rozpadła. W przeciągu 1 miesiąca, z inicjatywy Konrada "k0rku" Hoffmanna powstała organizacja Expl4ined która otrzymała nowe barwy i teraz walczy o swoje wysokie miejsce na scenie E-Sportowej</p>
